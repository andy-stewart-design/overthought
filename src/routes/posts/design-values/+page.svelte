		<h1 class="text-5xl font-medium leading-[1.2]">What is a product if not a big stack of compromises?</h1>
		<p>
			As product people, we’re often required to resolve disparities between two opposing forces—between what is
			theoretically and technically possible, between the needs of internal stakeholders and external users, between the
			blue-sky idea and the boots-on-the-ground implementation. If you work in software, this reality is likely a source
			of your anxiety and sadness, a creature lurking in your closet.
		</p>
		<p>
			The way that you conceive of and organize your work matters. These contradictions are an inevitable part of the
			job, unavoidable and unsolvable. Your only option is to learn to recognize them and devise strategies to manage
			them.
		</p>
		<p>These are mine.</p>
		<h2>End-to-end Engagement</h2>
		<p>
			We like to visualize them as straight lines, but projects are messy. There’s the false starts and dead ends.
			There’s the open-ended conversations, the seemingly endless number of ideas that must be reduced into a finite set
			of actionable tasks. And there’s the small requests and suggestions received in passing along the way, the types
			of things that can easily fall between the cracks.
		</p>
		<p>
			The best way that I’ve found to keep projects on the happy path—to the extent that that’s possible—is by having a
			person or small team involved from beginning to end. Rather than setting up projects like a game of telephone,
			with messages getting lost or mangled along the chain of transmission, I prefer to play the role of the
			translator. By being involved from ideation to implementation, I’m able to help guide and transition projects from
			one phase to the next, ensuring that what is proposed matches what is delivered.
		</p>
		<h2>Finding the Signal</h2>
		<p>
			There’s a refrain in Gabrielle Zevin’s <i>Tomorrow and Tomorrow and Tomorrow</i> about how you should design a game
			by imagining the person who will eventually play it. Which is true—we design products for the people who will use them,
			and we should keep users at the forefront of our concern.
		</p>
		<p>
			Unfortunately, end users are not the only stakeholders in our design process. Strategists need to see their
			recommendations faithfully implemented. Developers need designs that are technically sound. Businesses need a
			return on their investment. All these concerns do not neatly align—they must be weighed and balanced.
		</p>
		<p>
			Separating signal from noise when it comes to design inputs often feels like trying to cut a path through a
			thicket. In addition to being able to focus on feedback for an isolated feature or phase, I’m experienced with
			playing the role of facilitator, receiving inputs from all relevant stakeholders and working to synthesize them
			into discrete, actionable workflows.
		</p>
		<h2>Leaning Into the Medium</h2>
		<p>
			Figma is a triumph of design and engineering. It’s also a pile of abstractions. Auto layout is a partial
			implementation of the Flexbox API. Figma Variables are a quirky mashup of CSS Custom Properties and Javascript
			conditional logic. These features are powerful. They are also full or footguns. One of the dangers I’ve run into
			while onboarding other designers to Figma is that, if you’re not careful, you’ll start designing for the tool
			instead of designing for the medium.
		</p>
		<p>
			Teaching myself more about the software development process has benefited me as a designer, helping me to see past
			these abstractions and work in closer proximity to the final product. It allows me to develop more resilient
			systems and more informed solutions. It makes me less likely to design myself into a corner. In some cases, it
			directly improves the end product because I can assist with the implementation of a particularly tricky or nuanced
			interaction when timelines get tight.
		</p>
		<p>
			While I believe there’s value in approaching a problem with a beginner’s mind, free from the weight of experience
			and preconceptions, I have found equal—and more sustainable—value in investing the time to internalize a medium’s
			quirks and intricacies.
		</p>
		<h2>Blurring Boundaries</h2>
		<p>
			Have you looked at job boards recently? If so, you've likely seen companies preaching the value of
			"cross-functional collaboration," a term of art describing a process free of the silos and waterfalls of yore. I
			wholeheartedly agree that the best way to build better products is to, as much as possible, close the gap between
			design and development—and strategy, UX, and product management for that matter. And I want to help make that
			ideal a reality.
		</p>
		<p>
			One of the more effective ways I've found to enable cross-functional collaboration is through cross-functional
			people. As a designer with a competency for code and a background in journalism, I'm experienced in serving as a
			liaison between disciplines, and I'm personally invested in helping to bridge the communication divides that often
			exist on teams.
		</p>
		<p>
			I believe that the more my skill set overlaps with those of my colleagues, the easier it is for me to speak with
			and understand their needs and perspectives, and the more we’re able to accomplish together.
		</p>
		<h2>Building as a Form of Thinking</h2>
		<p>
			The debate over whether designers should learn to code is exhausting. I cannot give you a definitive answer—no one
			can. Like most questions posed as binaries, the answer is, “it depends.” Design and development are a spectrum,
			not a dichotomy.
		</p>
		<p>
			Here’s what I can tell you: coding makes me a better designer. It sharpens my senses, widens my perspective, and
			gives me new tools for expression and discovery. I'm able to prototype concepts in real time using the raw
			material of the medium. I can validate design solutions more quickly, with more precision, at a higher fidelity.
			In short, I spend less of my time chasing bad ideas down dead ends.
		</p>
		<p>
			I work with a bias towards action. Code, for me, is just one more tool that allows me to communicate design
			intent, bring notional ideas to life, and move more quickly from design documents and abstract concepts to
			tangible artifacts.
		</p>
